int		max(int* tab, unsigned int len)
{
	int i;
	int max;
	
		if(len = 0)
	{
		return 0;
	}
	
	max = 0;
	i = 0;
	while(i < len)
	{
		if(tab[i] >= max)
		{
			max = tab[i];
		}
		i++;
	}
	return max;
}