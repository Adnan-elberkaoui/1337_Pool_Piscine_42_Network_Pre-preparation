#include <unistd.h>

int main(void)
{
    char c;
    char upper;

    c = 'a';
    while (c <= 'z')
    {
        if ((c - 'a') % 2 == 0)
        {
            write(1,&c, 1);
        }
        else
        {
            upper = c - 32;
            write(1, &upper, 1);
        }
        c++;
    }
    write(1, "\n", 1);
    return (0);
}